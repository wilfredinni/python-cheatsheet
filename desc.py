# The pow() function is used to calculate the power of a number.
# It returns the result of raising the first number (base) to the power of the second number (exponent).
# Syntax ->pow(base,expo)
# It can also take a third argument (modulus) to return the result modulo that number.
# syntax -> pow(base,expo,mod)

print(pow(2,5))  # result: 2**5= 32

print(pow(2, 3, 5))    # result: 3 â†’ because 2**3 = 8, and 8 % 5 = 3